<?php

namespace Hexa\Units\Application\Update;

use Hexa\Shared\Domain\Bus\Command\Command;

final class UpdateUnitCommand implements Command
{
    private $id;
    private $name;

    public function __construct(int $id, string $name)
    {
        $this->id = $id;
        $this->name = $name;
    }

    public function id(): int
    {
        return $this->id;
    }

    public function name(): string
    {
        return $this->name;
    }
}
